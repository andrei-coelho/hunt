var fs   = require('fs'),
    path = require('path'),
    bot  = require('./modules/bot_control');

var log = require('./helpers/log');

global.appRoot = path.resolve(__dirname);

fs.readFile("./json/conf.json", (e, data) => {

    if (e) throw e;

    if(process.argv[2]){

        switch (process.argv[2]) {
            case "start":
                let conf = JSON.parse(data);
                bot.start(conf);
                break;
            default:
                break;
        }

    }
});
